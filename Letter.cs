        }

        public static void clearZone(int x, int y, int x1, int y1, ConsoleColor z) {
            Console.BackgroundColor = z;
            int y2 = y1 - y;
            int x2 = x1 - x;
            string k = "";
            for (int a = 0; a < x2; a++) { k += " "; }
            for (int a = 0; a < y2; a++) { Console.SetCursorPosition(x, y + a); Console.Write(k); }
            Console.ResetColor();
        }

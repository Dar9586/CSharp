                else if (a[b] == 'v') { x += mV(x, y, z) + 1; }
                else if (a[b] == 'w') { x += mW(x, y, z) + 1; }
                else if (a[b] == 'x') { x += mX(x, y, z) + 1; }
                else if (a[b] == 'y') { x += mY(x, y, z) + 1; }
                else if (a[b] == 'z') { x += mZ(x, y, z) + 1; }
                else if (a[b] == '0') { x += m0(x, y, z) + 1; }
                else if (a[b] == '1') { x += m1(x, y, z) + 1; }
                else if (a[b] == '2') { x += m2(x, y, z) + 1; }
                else if (a[b] == '3') { x += m3(x, y, z) + 1; }
                else if (a[b] == '4') { x += m4(x, y, z) + 1; }
